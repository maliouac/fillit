void	erase_last(char *map, char *tetri)
{
	int		i;
	char	c;

	i = 0;
	c = '0';
	while (tetri[i])
	{
		if (tetri[i] != '.' && tetri[i] != '\n')
			c = tetri[i];
		i++;
	}
	i = 0;
	while (map[i])
	{
		if (map[i] == c)
			map[i] = '.';
		i++;
	}
}
