int		minsquare(char **tab)
{
	int i;
	int j;
	int temp;

	i = 0;
	j = 1;
	while (tab[i])
		i++;
	i = 4 * i;
	temp = i;
	while (j)
	{
		if (i / j <= j)
			return (j);
		j++;
	}
	return (0);
}
